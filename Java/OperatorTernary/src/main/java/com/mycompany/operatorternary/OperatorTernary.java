package com.mycompany.operatorternary;

/**
 *
 * @author Yafig
 */
public class OperatorTernary {
    public static void main(String[] args) {
        
        boolean suka = true;
        String jawaban;

        // menggunakan operator ternary
        jawaban = suka ? "Ya" : "tidak";

        // menampilkan jawaban
        System.out.println(jawaban + " hobi saya adalah menonton film");

    }
}
